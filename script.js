let vampiro = document.querySelector('#vampiro');
let pauseVampiro = document.querySelector('#pauseVampiro');
let playVampiro = document.querySelector('#playVampiro');

pauseVampiro.addEventListener('click', () => {
    vampiro.pause()
    pauseVampiro.style.display = 'none'
    playVampiro.style.display = 'flex'
});

playVampiro.addEventListener('click', () => {
    vampiro.play()
    playVampiro.style.display = 'none'
    pauseVampiro.style.display = 'flex'
});

